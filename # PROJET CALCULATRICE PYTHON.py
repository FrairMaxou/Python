# Demander un premier chiffre
# Demander un second chiffre
# Faire une addition
# Display le résultat de l'addition

premier_chiffre = input("Veuillez entrer un premier nombre :")
second_chiffre = input("Veuilez entrer un second chiffre :")
resultat = float(premier_chiffre) + float(second_chiffre)
print(f"Le résultat de l'addition du nombre {premier_chiffre} avec le nombre {second_chiffre} est égal à {resultat}")